const Item = ({ title, to, icon, selected, setSelected }) => {
  // we're going to set up the theme and the colors  
  const theme = useTheme();
  const colors = tokens(theme.palette.mode);
  return (
    <MenuItem
      // if we have the title that's selected we're going to set that as active and that's what MenuItem requires from reactPro sidebar
      active={selected === title}
      style={{
        color: colors.grey[100],
      }}
      onClick={() => setSelected(title)}
      icon={icon}
    >
      <Typography>{title}</Typography>
      {/* with ReactPro sidebar you can pass in react router Dom link into it that gives you the access to use react router with the component that they've built
      */}
      <Link to={to} />
    </MenuItem>
  );
};

